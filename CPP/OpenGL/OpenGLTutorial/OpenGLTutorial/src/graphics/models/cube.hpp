        for (unsigned int i = 0; i < noVertices; i++) {
            indices[i] = i;
        }

        /*Texture tex("assets/flag.png", "material.diffuse");
        tex.load();
        Texture tex_specular("assets/flag_specular.png", "material.specular");
        tex_specular.load();*/

